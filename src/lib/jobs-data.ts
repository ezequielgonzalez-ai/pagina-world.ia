// Helper functions
export function getProfessionsByIndustry(industry: IndustryType): Profession[] {
  return professions.filter(prof => prof.industry === industry);
}

export function searchProfessions(query: string): Profession[] {
  const lowerQuery = query.toLowerCase();
  return professions.filter(prof => 
    prof.name.toLowerCase().includes(lowerQuery) ||
    prof.nameEs.toLowerCase().includes(lowerQuery) ||
    prof.description.toLowerCase().includes(lowerQuery)
  );
}

export function getProfessionById(id: string): Profession | undefined {
  return professions.find(prof => prof.id === id);
}

